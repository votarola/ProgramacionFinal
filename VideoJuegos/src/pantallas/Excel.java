    private JTable getTable_importar() {
		if (tablaventas == null) {
			tablaventas = new JTable();
			modelo = new DefaultTableModel(filas, columna);
			tablaventas.setModel(modelo);
			tablaventas.setAutoResizeMode(JTable.AUTO_RESIZE_OFF); 
			tablaventas.setEnabled(false); 
			tablaventas.setRowHeight(25); 
			tablaventas.setRowMargin(4); 
			tabla_ancho = modelo.getColumnCount() * 150; 
			tabla_alto = modelo.getRowCount() * 25; 
			tablaventas.setPreferredSize(new Dimension( tabla_ancho, tabla_alto));

			
			
		}
		return tablaventas;
	}
    
